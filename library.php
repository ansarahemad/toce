                                        <p>The Central Library of Theem College of Engineering is a treasure house of knowledge comprises ever growing collection of around 20000 books, journals and non-book material covering the principle fields of interest in engineering and related subjects like Electronics, Electronics & Telecommunication, Electrical, Computer Engineering, Information Technology, Mechanical, Civil, and Automobile etc. 
