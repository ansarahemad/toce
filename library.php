										<h4>Vision</h4>
                                        <p>Library plans to integrate the latest technology and adopt user friendly approach towards students and faculty. Library intends to offer comprehensive services related to dissemination of knowledge.</p>
										<h4>Mission</h4>
                                        <p>Library serves as a resource center and aims to develop a comprehensive collection of document’s useful for faculty and students of the institute and provides an efficient dissemination of knowledge. The primary mission of library is to support the educational and research programme of the institute. The secondary mission is, to serve as a resource center for the scholars and scientific community of the country. The ultimate mission of the library is to facilitate creation of new knowledge through organization and dissemination of knowledge and resources.</p>
										<h4>Short Term Goals</h4>
                                        <p>To serve the academic needs of all students and faculty members. As a center for collection of books and Journals of all related subjects to develop a comprehensive collection of information required for teaching and reference purpose. To acquire, organize Information resources to meet the present and future needs of its users.</p>
