										<br><br>The Library has an online journal Portal for Open Source Journals. A separate collection of more than 1778 CD's, Floppies and DVDs covering a vast range of subjects like Software, Communication Skills, Management skills, Personal Management, Quantitative Aptitude Test,  Aptitude test GATE, GRE, GMAT, TOEFL. It has also maintained University syllabus, Previous Year question papers/Question Banks, Old Project Reports list, and Manuals, Handbooks and Newspapers Clippings are also available in library. All these valuable resources are well organized in the 12000 Sq. Ft. areas, where more than 460 users can sit at a time and they are monitored by CC TV Cameras.</p>
